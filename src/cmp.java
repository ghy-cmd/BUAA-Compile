public class cmp {
    String l;
    String r;
    String op;

    public cmp(String l, String r, String op) {
        this.l = l;
        this.r = r;
        this.op = op;
    }

    public String getL() {
        return l;
    }

    public String getOp() {
        return op;
    }

    public String getR() {
        return r;
    }
}
